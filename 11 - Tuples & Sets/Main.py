# Tuples - Immutable sequences, typically used to store collections of heterogeneous data.
# Tuples are defined by enclosing the elements in parentheses ().
# Tuples don't have properties like append, remove, or sort because they cannot be modified after creation.
data_tuples = (1, 2, 3, 4, 5)
print(data_tuples)
print(data_tuples[1])

# Sets - Unordered collections of unique elements.
# Sets are defined by enclosing the elements in curly braces {} or by using the set() function
# Sets support operations like union, intersection, and difference.
# Sets do not allow duplicate elements.
# Sets do not support indexing or slicing because they are unordered.
data_sets = {5, 2, 3, 4, 1}
print(data_sets)